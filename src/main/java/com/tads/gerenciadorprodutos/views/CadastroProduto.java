    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Produto produto = new Produto();
        produto.setId(produtoId);
        produto.setNome(TextFieldNome.getText());
        produto.setDescricao(jTextAreaDescricao.getText());
        String precoCompra = jTextFieldPrecoCompra.getText();
        produto.setPrecoCompra(Float.parseFloat(precoCompra.replace("R$", "").replace(",", ".")));
        String precoVenda = jTextFieldPrecoVenda.getText();
        produto.setPrecoVenda(Float.parseFloat(precoVenda.replace("R$", "").replace(",", ".")));
        String quantidade = jTextFieldQuantidade.getText();
        produto.setQuantidade(Integer.parseInt(quantidade));

        try {
            if (isAlterar) {
                ServicoProduto.atualizarProduto(produto);
                JOptionPane.showMessageDialog(rootPane,
                        "Produto alterado com sucesso",
                        "Alteração efetuada",
                        JOptionPane.INFORMATION_MESSAGE);
            } else {
                ServicoProduto.cadastrarProduto(produto);
                JOptionPane.showMessageDialog(rootPane,
                        "Produto inserido com sucesso",
                        "Cadastro efetuado",
                        JOptionPane.INFORMATION_MESSAGE);
            }
            limpar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(),
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

