    private void limpar() {
        TextFieldNome.setText("");
        jTextAreaDescricao.setText("");
        jTextFieldPrecoCompra.setText("R$ 0,00");
        jTextFieldPrecoVenda.setText("R$ 0,00");
        jTextFieldQuantidade.setText("0");
    }

    public void alterarProduto(Produto produto){
        TextFieldNome.setText(produto.getNome());
        jTextAreaDescricao.setText(produto.getDescricao());
        jTextFieldPrecoCompra.setText("R$ " + produto.getPrecoCompra());
        jTextFieldPrecoVenda.setText("R$ " + produto.getPrecoVenda());
        jTextFieldQuantidade.setText("" + produto.getQuantidade());
        produtoId = produto.getId();
        isAlterar = true;
    }
    

